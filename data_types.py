x = 3 + 5j

# Text - str
x = "hello"
x = 'hello'
x = "hello world, this is python programming"
x = '''hello world'''
x = """hello world"""

# Boolean - True/False, True = 1, False = 0
x = True
x = False

# Bytes
x = b'hello'
x = b'12'

# Data Collections - List, Tuples, Dictionary, Set
x = [12,45,7,3,5,7,8,9,3,12,7,9] # List
x = (12,45,7,3,5,7,8,9,3,12,7,9) # Tuple
